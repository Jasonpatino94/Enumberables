class Array

    def my_each(&prc)
        i = 0
        while i < self.length
            prc.call(self[i])
            i += 1
        end
        self
    end

    def my_select(&prc)
        selected = []

        self.my_each do |ele|
            selected << ele if prc.call(ele) == true
        end
    end

end


